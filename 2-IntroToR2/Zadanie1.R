**Cel:** Poznanie interfejsu i podstawowych funkcji RStudio.

### Instrukcje i rozwiązanie:

1.  **Uruchom RStudio** na swoim komputerze.

2.  **Zapoznaj się z interfejsem użytkownika:**
  
  3.  **Stwórz nowy skrypt R:**
  
  4.  **W skrypcie napisz komentarze** opisujące funkcje poszczególnych paneli.

  #Source (edytor skryptu)- służy do pisania skryptów, a funkcje nie są automatycznie wykonywane
  #Console (konsola) służy- do wyświetlania już uruchomionych funkcji
  #Enviroment (środowisko)- wyświetla wszystkie przypisane wartości
  #Output panel- służy do otwierania oraz wyświetlania plików z komputera

